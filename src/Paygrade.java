    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
            conn = Database.getConnection();
            int id = Integer.parseInt(txt_gradeid.getText());
            float basic = Float.parseFloat(txt_basic.getText());
            float ta = Float.parseFloat(txt_ta.getText());
            float da = Float.parseFloat(txt_da.getText());
            float hra = Float.parseFloat(txt_hra.getText());
            float ma = Float.parseFloat(txt_ma.getText());
            float bonus = Float.parseFloat(txt_bonus.getText());
            float pf = Float.parseFloat(txt_pf.getText());
            float pt = Float.parseFloat(txt_pt.getText());
            float total = pf+pt;
            PreparedStatement pst = conn.prepareStatement("Insert Into PAYMENT.PAYGRADE VALUES (?,?,?,?,?,?,?,?,?)");
            pst.setInt(1,id);
            pst.setFloat(2,basic);
            pst.setFloat(3,ta);
            pst.setFloat(4,da);
            pst.setFloat(5,hra);
            pst.setFloat(6,ma);
            pst.setFloat(7,bonus);
            pst.setFloat(8,pf);
            pst.setFloat(9,pt);
            txt_total.setText(total+"$");
            pst.execute();
            JOptionPane.showMessageDialog(null,"Data added!");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

