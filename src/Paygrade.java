    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            conn=Database.getConnection();
            PreparedStatement pst = conn.prepareStatement("SELECT * FROM PAYMENT.PAYGRADE JOIN PAYMENT.PAYROLL ON PAYGRADE.GRADE_ID = PAYROLL.grade_id JOIN DETAILS.EMPLOYEE ON EMPLOYEE.emp_id = PAYROLL.emp_id WHERE EMPLOYEE.emp_id = ?");
            int id = Integer.parseInt(txt_empid.getText());
            pst.setInt(1,id);   
            rs = pst.executeQuery();
            while(rs.next()){
            String add1 =rs.getString("grade_id");
            txt_gradeid.setText(add1);

            String add2 =rs.getString("grade_basic");
            txt_basic.setText(add2+"$");

            String add3 =rs.getString("grade_ta");
            txt_ta.setText(add3+"$");

            String add4 =rs.getString("grade_da");
            txt_da.setText(add4+"$");

            String add5 =rs.getString("grade_ma");
            txt_ma.setText(add5+"$");
            String add9 = rs.getString("grade_hra");
            txt_hra.setText(add9+"$");

            String add6 =rs.getString("grade_bonus");
            txt_bonus.setText(add6+"$");

            String add7 =rs.getString("grade_pf");
            txt_pf.setText(add7+"$");

            String add8 =rs.getString("grade_pt");
            txt_pt.setText(add8+"$");
            
            float pf = Float.parseFloat(add7);
            float pt = Float.parseFloat(add8);
            float total = pf + pt;
            String ts = String.valueOf(total);
            txt_total.setText(ts+"$");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

